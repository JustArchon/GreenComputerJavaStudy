package GradeManagement;

public class Student {
	private String name;
	private String stulevel;
	private String stuclassnum;
	private String phonenumber;
	private String email;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getStulevel() {
		return stulevel;
	}
	public void setStulevel(String stulevel) {
		this.stulevel = stulevel;
	}
	public String getStuclassnum() {
		return stuclassnum;
	}
	public void setStuclassnum(String stuclassnum) {
		this.stuclassnum = stuclassnum;
	}
	public String getPhonenumber() {
		return phonenumber;
	}
	public void setPhonenumber(String phonenumber) {
		this.phonenumber = phonenumber;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
}
